        String capacity = this.jTextField4.getText();
        String floor = this.jComboBox1.getSelectedItem().toString();
        
        if(jTextField3.getText().isEmpty() && jTextField1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"No Block Selected..!!!\nPlease Enter a Valid Block Number");
        }
