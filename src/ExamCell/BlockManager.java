    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try
        {
            String block = this.jTextField3.getText();
            String sql = "select count(Block_No) from Block_Details where Block_No = '"+block+"'";
            pst=conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            String rowCount = rs.getString(1);
            
            rowCounted = Integer.parseInt(rowCount);
                       
        }
        catch(NumberFormatException | SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally
        {
            try {
                rs.close();
                pst.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,ex);
            }
            updateTable();
        }
        if(rowCounted>0){
            System.out.println(rowCounted);
            JOptionPane.showMessageDialog(null,"Cannot add Block Details multiple times ..!!");
        }
        else{
            int x = JOptionPane.showConfirmDialog(null, "Are you sure you want to add New Block Record","Add New Block Record",JOptionPane.YES_NO_OPTION);
            
            if(jTextField4.getText().isEmpty() || jTextField3.getText().isEmpty() || jComboBox1.getSelectedItem().toString().equals("Select an Option"))
            {
                JOptionPane.showMessageDialog(null,"Please Fill up all Fields and Lists ..!!");
                x=1;
            }
            else{
                
                if(x==0)
                {
                    try {
                        String sql = "INSERT INTO Block_Details(Block_No,Floor,Capacity)VALUES(?,?,?)";
                        
                        pst = conn.prepareStatement(sql);
                        
                        pst.setString(1, this.jTextField3.getText());
                        pst.setString(3, this.jTextField4.getText());
                        pst.setString(2, this.jComboBox1.getSelectedItem().toString());
                        
                        pst.execute();
                                                
                        JOptionPane.showMessageDialog(null,"New Block Record Added Sucessfully");
                                                
                    } 
                    catch (HeadlessException | SQLException e) 
                    {
                        JOptionPane.showMessageDialog(null, e);
                    }
                    
                    finally
                    {
                        try
                        {
                            rs.close();
                            pst.close();
                        }
                        catch(SQLException ex)
                        {
                            JOptionPane.showMessageDialog(null,ex);
                        }
                        updateTable();
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

